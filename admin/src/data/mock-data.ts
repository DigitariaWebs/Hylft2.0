// Types
export interface User {
  id: string;
  username: string;
  email: string;
  avatar: string;
  bio: string;
  gender: "male" | "female";
  joinDate: string;
  status: "active" | "inactive" | "banned";
  followers: number;
  following: number;
  postsCount: number;
  workoutsCount: number;
  experienceLevel: "beginner" | "intermediate" | "advanced";
  fitnessGoal: string;
  units: "metric" | "imperial";
  age: number;
  weight: number;
  height: number;
}

export interface Workout {
  id: string;
  userId: string;
  userName: string;
  name: string;
  date: string;
  duration: number;
  exerciseCount: number;
  caloriesBurned: number;
  notes: string;
}

export interface Routine {
  id: string;
  userId: string;
  userName: string;
  name: string;
  description: string;
  exerciseCount: number;
  difficulty: "beginner" | "intermediate" | "advanced";
  targetMuscles: string[];
  timesCompleted: number;
  estimatedDuration: number;
}

export interface Post {
  id: string;
  userId: string;
  userName: string;
  caption: string;
  likes: number;
  comments: number;
  timestamp: string;
  status: "published" | "flagged" | "removed";
  imageCount: number;
}

export interface DashboardStats {
  totalUsers: number;
  activeUsers: number;
  totalWorkouts: number;
  totalRoutines: number;
  totalPosts: number;
  newUsersThisWeek: number;
  workoutsThisWeek: number;
  flaggedPosts: number;
}

export interface RecentActivity {
  id: string;
  type:
    | "user_joined"
    | "workout_completed"
    | "post_flagged"
    | "routine_created";
  description: string;
  timestamp: string;
  userName: string;
}

